<section id="steps">
  <h2>Highlight steps</h2>
  <p>
    A slide is not just code; it is a sequence. Highlight steps let you dim the
    unrelated lines, enlarge the part you are talking about, and play those states in
    order with the arrow keys.
  </p>

  <h3 id="steps-seq">Step sequencing</h3>
  <p>
    Each step dims everything outside the selection and brings the selected code
    forward. Number keys <kbd>1</kbd>–<kbd>9</kbd> jump straight to a step;
    <kbd>0</kbd> returns to the clean, un-highlighted slide.
  </p>

  <h3 id="steps-toolbar">Selection toolbar</h3>
  <p>
    Select code and a toolbar appears to create a highlight from the selection.
    Right-click is retained as a fallback for the same action.
  </p>

  <h3 id="steps-dnd">Drag to reorder</h3>
  <p>
    Steps can be reordered by drag-and-drop, with keyboard arrow-button controls as the
    accessible equivalent.
  </p>
</section>
